<h2>What is Asgard?</h2>
<p>Asgard is a role playing game complete with interactive landscapes, towns, quests, and dynamic storylining on an all-encompassing graphical map.</p>
<br />

<h2>How do I pronounce Asgard?</h2>
<p>Pronounce it like you're choking on a potato.  Ahz-guard.</p>
<br />

<h2>Is Asgard playable?</h2>
<p>No--Asgard is still in pre-development.  The development team is working on basic elements like reading and writing map data from a database.  The story-writing team is still hammering out the fundamental structure of the flow of the game.</p>
<br />

<h2>When will Asgard be playable?</h2>
<p>It is not certain when Asgard will be playable.  Currently, the project is pre-alpha, with a lot of effort being spent on designing the software and organizing our limited resources.  Expect an interactive testing/debug client soon.</p>
<br />

<h2>Can I contribute text or media to Asgard?</h2>
<p>YES!  Designing and writing software is only a small part of making games.  Media: text, images, music and sounds, are all necessary components of a complete piece.  In order to protect your rights as an artist, please license your work using a <a href="http://creativecommons.org/">Creative Commons</a> license. Please see the <a href="http://wiki.creativecommons.org/Frequently_Asked_Questions">Creative Commons FAQ</a> for more information.</p>
<br />

<h2>Can I join the development team?</h2>
<p>YES!  However, you should probably look at the code and experiment with it before contacting the project.  When you are ready, please send an email to <a href="mailto:asgard.team@gmail.com">asgard.team@gmail.com</a>.</p>
<br />

<h2>Questions?</h2>
<p>Please email <a href="mailto:asgard.team@gmail.com">asgard.team@gmail.com</a> with any questions you may have.</p>

