		<h2>Releases and Milestones</h2>
		<ul>
		<li><p>MS1; LevelMyth.	In this milestone a console and accompanying parser will be introduced to, in the future, accept low-level commands.  Additionally, a type of structure for storing these commands will be created so that the commands can be passed throughout the system as messages.  Completed 02/15/2008.</p></li>
		<li><p>MS2; Quotationser.	A system for routing the messages introduced in the last release will be created.  This new mechanism will work similar to a hub in a computer network, delivering all messages it receives to each component of the system.	Completed 04/15/2008.</p></li>
		<li><p>MS3; Rrokuryook.  Further refining will be made to the messaging system.	System components will be clearly defined for the first time.	Additionally, our first command string will be able to be accepted, interpreted, sent to the controller component and processed.</p></li>
		<li><p>Minor Release; Asgard 0.3.1.  With this release developers will be able to tell the system via a console to load objects that will appear on the map from the database via a console command.</p></li>
		<li><p>MS1; ?.  A system for storing and loading animations will be designed, developed and implemented.</p></li>
		<li><p>MS2; ?.  Simultaneous to MS1, a second system will be introduced to display these animations in layers, respecting depth, onto the screen.</p></li>
		<li><p>MS3; ?.  A command message interface language, based on the constraints introduced in Asgard 0.3.1, will be designed and implemented.  This language of commands will trigger the systems created for MS1 and MS2 to operate.</p></li>
		<li><p>Minor Release; Asgard 0.3.2.  With this release developers will be able to store, load, and display animations.	A small test environment will exist to demo off the new functionality.</p></li>
