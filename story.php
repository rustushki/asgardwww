<h3>Synopsis</h3>
<p>Long ago, a rotten man named Vear brought our world of Brant into an age of
despair and ignorance.  At the height of a series of horrible attacks, a
fearless woman and her children were able to seal Vear into the Void.  But the
damage had been done: our sophisticated culture was brought to its knees.</p>

<p>Our stories, our history, our homelands were forever lost.  Vear took it all
away.  Now ages have come and gone and the seal has erroded.  Vear's madness
discreetly touches all aspects of our society.</p>

<p>The last of the Skraelingar attack supply caravans.  Markland and Helluland
are at war.  Terrible creatures emerge from water and caves and descend from
above the clouds.</p>

